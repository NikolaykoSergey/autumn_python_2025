x = int(input('Введите x: '))
y = int(input('Введите y: '))
print('Сумма: ' + str(x + y))
print('Разность: ' + str(x - y)) #Если требуется от большего отнять меньшее, то необходимо делать через цикл сравнения (в условии нет четкого указания на этот счет)
print('Частное: ' + str(x / y))
print('Произведение: ' + str(x * y))
print('Целочисленное деление: ' + str(x // y))
print('Деление с остатком: ' + str(x % y))
print('Возведение в степень: ' + str(x ** y))